package com.kh.chap03_override.model.vo;

public class Book /*extends Object*/ {

	private String title;
	private String author;
	private int price;
	
	// 기본생성자
	public Book() {
		
	}
	
	// 매개변수 생성자
	public Book(String title, String author, int price) {
		this.title = title;
		this.author = author;
		this.price = price;
	}
	
	// getter/setter 메소드
	public void setTitle(String title) {
		this.title = title;
	}
	public void setAuthor(String author) {
		this.author = author;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	
	public String getTitle() {
		return title;
	}
	public String getAuthor() {
		return author;
	}
	public int getPrice() {
		return price;
	}
	
	/*
	 * * 오버라이딩
	 * - 자식클래스가 상속받고 있는 부모클래스의 메소드를 재정의(재작성)하는 것.
	 * - 부모가 제공하는 메소드를 자식이 일부 고쳐서 사용하겠다는 의미
	 * 	  자식객체를 통해 실행 시 자식메소드가 우선권을 가짐
	 * 
	 * * 오버라이딩 성립 조건
	 * - 부모메소드명 동일
	 * - 매개변수 갯수, 자료형, 순서 동일(매개변수명은 상관없음)
	 * - 부모메소드의 반환형 통일 (jdk 버전업이 되면서 부모메소드의 반환형의 자식 자료형들로는 가능)
	 * - 부모메소드의 접근제한자보다 범위가 같거나 커야한다.
	 * 		ex) 부모메소드의 접근제한자가 protected일 경우 오버라이딩하는 메소드의 접근제한자는 protected, public
	 * 
	 * => 규약의 개념이 들어가있음 (재정의 하려면 이 정도의 규칙은 지켜줘야한다~~)
	 * 
	 * * @Override 어노테이션
	 * - 생략 가능. 크게 상관 없음 (명시하지 않아도 오버라이딩 된 상태)
	 * - 어노테이션 작성하는 이유
	 * 	  >> 부모메소드명이나 매개변수가 수정될 경우 => 찾아보기 쉽다.
	 * 		  자식메소드 기술 시 오타가 있을 경우 => 찾아보기 쉽다.
	 * 	  >> 단지 오버라이딩한 메소드라는 것을 알리기 위해.
	 */
	
	@Override
	public String toString() { // Object 클래스의 toString 메소드 오버라이딩
		return "title: " + title + ", author: " + author + ", price: " + price;
	}
}
